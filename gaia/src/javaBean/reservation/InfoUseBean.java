package javaBean.reservation;

public class InfoUseBean {

	private String SITE_NUM;
	private String SITE_NAME;
	private String SITE_SIZE;
	private String SITE_INFO;
	private String DIFF;
	private int TOTAL;
	
	public String getSITE_NUM() {
		return SITE_NUM;
	}
	public void setSITE_NUM(String sITE_NUM) {
		SITE_NUM = sITE_NUM;
	}
	public String getSITE_NAME() {
		return SITE_NAME;
	}
	public void setSITE_NAME(String sITE_NAME) {
		SITE_NAME = sITE_NAME;
	}
	public String getSITE_SIZE() {
		return SITE_SIZE;
	}
	public void setSITE_SIZE(String sITE_SIZE) {
		SITE_SIZE = sITE_SIZE;
	}
	public String getSITE_INFO() {
		return SITE_INFO;
	}
	public void setSITE_INFO(String sITE_INFO) {
		SITE_INFO = sITE_INFO;
	}
	public String getDIFF() {
		return DIFF;
	}
	public void setDIFF(String dIFF) {
		DIFF = dIFF;
	}
	public int getTOTAL() {
		return TOTAL;
	}
	public void setTOTAL(int tOTAL) {
		TOTAL = tOTAL;
	}
}