package javaBean.product;

public class ProductBean {
	private String P_NUM;
	private String P_NAME;
	private int P_PRICE;
	private String P_SERVICE;
	private String P_NOTES;
	private String P_MORE_INFO;
	private String P_IMG;
	
	public String getP_NUM() {
		return P_NUM;
	}
	public void setP_NUM(String p_NUM) {
		P_NUM = p_NUM;
	}
	public String getP_NAME() {
		return P_NAME;
	}
	public void setP_NAME(String p_NAME) {
		P_NAME = p_NAME;
	}
	public int getP_PRICE() {
		return P_PRICE;
	}
	public void setP_PRICE(int p_PRICE) {
		P_PRICE = p_PRICE;
	}
	public String getP_SERVICE() {
		return P_SERVICE;
	}
	public void setP_SERVICE(String p_SERVICE) {
		P_SERVICE = p_SERVICE;
	}
	public String getP_NOTES() {
		return P_NOTES;
	}
	public void setP_NOTES(String p_NOTES) {
		P_NOTES = p_NOTES;
	}
	public String getP_MORE_INFO() {
		return P_MORE_INFO;
	}
	public void setP_MORE_INFO(String p_MORE_INFO) {
		P_MORE_INFO = p_MORE_INFO;
	}
	public String getP_IMG() {
		return P_IMG;
	}
	public void setP_IMG(String p_IMG) {
		P_IMG = p_IMG;
		
	}

}
